    private void HitungHari() {
    if (Tahun.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Sebelum melanjutkan, pastikan tahun telah diisi dengan benar.", "Peringatan",
        JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    int thn = Integer.parseInt(Tahun.getText());
    String selectedBulan = Bulan.getSelectedItem().toString();
    
    jum_hari = HitungHari.hitung(thn, selectedBulan); // Panggil metode hitung dari kelas HitungHari
    
    hasil = "Jumlah Hari Pada Bulan " + selectedBulan + " Tahun " + Tahun.getText() + " Adalah " + jum_hari;
    jLabel4.setText(hasil); // Update JLabel untuk menampilkan hasil
    
    BtnSimpan.setEnabled(true);
    BtnHapus.setEnabled(true);
}
    

    private void BtnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            String workingDir = System.getProperty("user.dir");
            String filePath = workingDir + File.separator + "HitungHari.txt";
            BufferedWriter out = new BufferedWriter(new FileWriter(filePath));
            String hasil = "Jumlah Hari pada bulan " + Bulan.getSelectedItem() + " Tahun " + Tahun.getText()+ " Adalah " + jum_hari;
            out.write(hasil);

            JOptionPane.showMessageDialog(this, "File Berhasil Disimpan di: " + filePath);
            out.close();
            } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data Belum Ada", "Pesan", JOptionPane.ERROR_MESSAGE);
        }

        
    }//GEN-LAST:event_BtnSimpanActionPerformed


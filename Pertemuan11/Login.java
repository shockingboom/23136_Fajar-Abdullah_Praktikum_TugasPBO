    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
        String userId = id.getText();
        String userPw = pw.getText();
        
        try {
            Connection connection = Koneksi.konek();
            System.out.println("Koneksi berhasil!");

        // Menggunakan PreparedStatement untuk keamanan
            String query = "SELECT * FROM user WHERE id_user = ? AND password = ?";
            PreparedStatement preparedStatement = connection.prepareStatement(query);
            preparedStatement.setString(1, userId);
            preparedStatement.setString(2, userPw);

            ResultSet resultSet = preparedStatement.executeQuery();
            if(resultSet.next()){
                System.out.println("Login berhasil");
                new Kalkulator().setVisible(true);
                dispose();
                // Tambahkan logika setelah login berhasil, misalnya membuka jendela utama aplikasi
            } else {
            System.out.println("Username atau password salah");
            }

            // Menutup resources
            resultSet.close();
            preparedStatement.close();
            connection.close();

        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(getClass().getName()).log(Level.SEVERE, null, ex);
            System.out.println("Error: " + ex.getMessage());
        } 
    }//GEN-LAST:event_loginActionPerformed

